// ---------- Adobe Accessibility Report reviews ----------

/**
 * Status of a single rule in an Adobe Accessibility Report. Adobe uses
 * "Passed", "Failed", "Needs manual check", "Skipped", "Not applicable" —
 * we normalize those to three buckets here.
 */
export type PdfRuleStatus = "failed" | "needs-check" | "passed" | "not-applicable";

export interface PdfFinding {
  id: string;
  /** Broad category from the Adobe report, e.g. "Document", "Page content" */
  category: string;
  /** Specific rule name, e.g. "Tagged PDF", "Logical Reading Order" */
  rule: string;
  status: PdfRuleStatus;
  /** The raw description/why text from the Adobe report (when available). */
  description?: string;
  /**
   * Plain-language, tool-agnostic suggestion for what to change in the source
   * file so this rule passes on the next export. Generated by the suggestion
   * library in lib/adobe-suggestions.ts.
   */
  suggestion: string;
  /** Whether the user has marked this finding as resolved (UI state). */
  resolved: boolean;
}

export interface PdfReview {
  id: string;
  pdfName: string;
  reportName: string;
  /** How big the original PDF was — shown in the dashboard row. */
  pdfSize: string;
  uploadedAt: string;
  /** Rough score: 100 minus weighted deductions for failures + manual checks. */
  score: number;
  findings: PdfFinding[];
}

