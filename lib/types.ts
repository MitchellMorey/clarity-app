export type DocType = "docx" | "pptx";

export type IssueCategory = "contrast" | "font" | "alt" | "heading";
export type IssueSeverity = "critical" | "warning" | "info";

export interface Issue {
  id: string;
  category: IssueCategory;
  severity: IssueSeverity;
  title: string;
  location: string;
  detail?: string;
  suggestion?: string;
  // Contrast-specific fields
  fg?: string;
  bg?: string;
  ratio?: string;
  required?: string;
  /**
   * Optional visual preview of the problem area — a snippet from the user's
   * document rendered with its detected formatting (Calibri-family font,
   * detected colors + size + weight). Captured by the real DOCX analyzer;
   * older mock issues won't have this.
   */
  preview?: IssuePreview;
  resolved: boolean;
}

export interface IssuePreview {
  /** The actual text from the user's document */
  text: string;
  /** Normalized hex foreground color, e.g. "#e6e6e6" */
  fg?: string;
  /** Normalized hex background color, e.g. "#ffffff" */
  bg?: string;
  /** Font size in points, e.g. 11 or 8 */
  sizePt?: number;
  /** The detected font family name (e.g. "Calibri", "Arial", "Times New Roman") */
  fontFamily?: string;
  bold?: boolean;
  italic?: boolean;
  /** For heading issues: the detected heading level (1-9) */
  headingLevel?: number;
}

export interface ReviewVersion {
  version: number;
  reviewedAt: string;
  score: number;
  issues: Issue[];
  /** Number resolved since the previous version */
  resolvedSinceLast?: number;
}

export interface Document {
  id: string;
  name: string;
  type: DocType;
  size: string;
  uploadedAt: string;
  versions: ReviewVersion[];
}

